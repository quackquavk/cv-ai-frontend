/**
 * Get all keywords as a flat array
 * @returns {string[]} Array of all search keywords
 */
export function getAllKeywords() {
  const allKeywords = [];

  Object.values(seoKeywords).forEach((category) => {
    if (Array.isArray(category)) {
      allKeywords.push(...category);
    }
  });

  // Remove duplicates and return
  return [...new Set(allKeywords)];
}

/**
 * Get keywords by category
 * @param {string} categoryName - Name of the category
 * @returns {string[]} Array of keywords for that category
 */
export function getKeywordsByCategory(categoryName) {
  return seoKeywords[categoryName] || [];
}

/**
 * Get keyword count statistics
 * @returns {Object} Statistics about keywords
 */
export function getKeywordStats() {
  const stats = {
    total: 0,
    byCategory: {},
  };

  Object.entries(seoKeywords).forEach(([category, keywords]) => {
    if (Array.isArray(keywords)) {
      stats.byCategory[category] = keywords.length;
      stats.total += keywords.length;
    }
  });

  return stats;
}

export default seoKeywords;
